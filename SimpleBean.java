package com.niit.emirates;

public class SimpleBean {

	private String name=null;
	private int age=0;
	
	public SimpleBean()
	{
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
}
